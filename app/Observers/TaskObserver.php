<?php

namespace App\Observers;

use App\Task;
use App\Observers\helpers\EventModelMethods;

class TaskObserver
{
    use EventModelMethods;
    
    /**
     * Handle the task "created" event.
     *
     * @param  \App\Task  $task
     * @return void
     */
